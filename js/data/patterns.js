// パターン図解 — 「答えがパターンで分かれる」論点を、順番に動く図解（ステップ）で示す。
// 各要素は data-from（この手順から表示）/ data-until（この手順まで表示）で出し入れし、
// app.js の renderPattern が手順を進めて動画風に見せる。勘違いしやすい型は最後に大きな×。
// 図はSVG。法律関係（当事者A/B/C・譲渡・取消・登記）を箱と矢印で表す。

// ---- SVGビルダー（座標系 viewBox 0 0 360 300）----
function _g(from, until, inner) {
  const u = until === undefined ? "" : ` data-until="${until}"`;
  return `<g data-from="${from}"${u}>${inner}</g>`;
}
function _box(x, y, title, sub, from, until, hi) {
  return _g(
    from,
    until,
    `<rect x="${x}" y="${y}" width="92" height="46" rx="9" fill="${hi ? "#10243a" : "#232b33"}" stroke="${hi ? "#4fc3f7" : "#2e3942"}" stroke-width="2"/>
     <text x="${x + 46}" y="${y + 21}" text-anchor="middle" fill="#e8edf2" font-size="14" font-weight="bold">${title}</text>
     <text x="${x + 46}" y="${y + 37}" text-anchor="middle" fill="#8fa0ae" font-size="10">${sub || ""}</text>`,
  );
}
function _arrow(x1, y1, x2, y2, label, from, until, color) {
  color = color || "#8fa0ae";
  const mx = (x1 + x2) / 2,
    my = (y1 + y2) / 2;
  // ラベルはカード列の上の余白（y<36）に置く。カード間の隙間(28px)より文字が長く、
  // 矢印線上に置くとカードへ重なってしまうため、上の空きスペースへ逃がす。
  const ly = Math.min(y1, y2) - 33;
  return _g(
    from,
    until,
    `<line x1="${x1}" y1="${y1}" x2="${x2}" y2="${y2}" stroke="${color}" stroke-width="2.5" marker-end="url(#ah)"/>
     <text x="${mx}" y="${ly}" text-anchor="middle" fill="${color}" font-size="11" font-weight="bold">${label}</text>`,
  );
}
function _note(x, y, text, from, until, color) {
  color = color || "#e8edf2";
  return _g(
    from,
    until,
    `<text x="${x}" y="${y}" fill="${color}" font-size="12">${text}</text>`,
  );
}
function _mark(x, y, type, from, until) {
  const c = type === "x" ? "#ef5350" : "#66bb6a";
  const sym = type === "x" ? "✕" : "◯";
  return _g(
    from,
    until,
    `<text x="${x}" y="${y}" text-anchor="middle" fill="${c}" font-size="56" font-weight="bold" opacity="0.92">${sym}</text>`,
  );
}
// 中央寄せキャプション（イラストのラベル用。重なり回避のため位置を厳密に置く）
function _cap(x, y, text, from, until, color, size) {
  color = color || "#e8edf2";
  size = size || 11.5;
  return _g(
    from,
    until,
    `<text x="${x}" y="${y}" text-anchor="middle" fill="${color}" font-size="${size}" font-weight="bold">${text}</text>`,
  );
}
// ---- イラスト部品（建物・土地・人・登記簿）。手順と連動して出し入れする ----
// 当事者カード（人物アイコン入り。_box と同じ寸法・引数で差し替え可能）
function _party(x, y, name, sub, from, until, hi) {
  const c = hi ? "#4fc3f7" : "#9fb0bd";
  return _g(
    from,
    until,
    `<rect x="${x}" y="${y}" width="92" height="46" rx="9" fill="${hi ? "#10243a" : "#232b33"}" stroke="${hi ? "#4fc3f7" : "#2e3942"}" stroke-width="2"/>
     <circle cx="${x + 16}" cy="${y + 16}" r="6.5" fill="none" stroke="${c}" stroke-width="1.8"/>
     <path d="M${x + 7},${y + 35} Q${x + 16},${y + 22} ${x + 25},${y + 35}" fill="none" stroke="${c}" stroke-width="1.8"/>
     <text x="${x + 58}" y="${y + 20}" text-anchor="middle" fill="#e8edf2" font-size="15" font-weight="bold">${name}</text>
     <text x="${x + 46}" y="${y + 39}" text-anchor="middle" fill="#8fa0ae" font-size="9">${sub || ""}</text>`,
  );
}
function _house(x, y, from, until, label, hi) {
  const c = hi ? "#4fc3f7" : "#cfd8df";
  const fill = hi ? "#10243a" : "#2b333b";
  return _g(
    from,
    until,
    `<polygon points="${x},${y} ${x + 27},${y - 22} ${x + 54},${y}" fill="${fill}" stroke="${c}" stroke-width="2"/>
     <rect x="${x + 6}" y="${y}" width="42" height="36" fill="${fill}" stroke="${c}" stroke-width="2"/>
     <rect x="${x + 21}" y="${y + 15}" width="13" height="21" fill="${c}"/>
     <rect x="${x + 11}" y="${y + 6}" width="9" height="9" fill="none" stroke="${c}" stroke-width="1.5"/>
     ${label ? `<text x="${x + 27}" y="${y + 51}" text-anchor="middle" fill="#e8edf2" font-size="11.5" font-weight="bold">${label}</text>` : ""}`,
  );
}
function _land(x, y, w, from, until, label, color) {
  color = color || "#66bb6a";
  return _g(
    from,
    until,
    `<rect x="${x}" y="${y}" width="${w}" height="20" rx="3" fill="#19241a" stroke="${color}" stroke-width="2"/>
     <line x1="${x}" y1="${y + 20}" x2="${x + w}" y2="${y + 20}" stroke="${color}" stroke-width="3"/>
     ${label ? `<text x="${x + w / 2}" y="${y + 14}" text-anchor="middle" fill="${color}" font-size="11" font-weight="bold">${label}</text>` : ""}`,
  );
}
function _person(x, y, from, until, label, color) {
  color = color || "#4fc3f7";
  return _g(
    from,
    until,
    `<circle cx="${x}" cy="${y}" r="8" fill="none" stroke="${color}" stroke-width="2"/>
     <path d="M${x - 11},${y + 26} Q${x},${y + 9} ${x + 11},${y + 26}" fill="none" stroke="${color}" stroke-width="2"/>
     ${label ? `<text x="${x}" y="${y + 40}" text-anchor="middle" fill="${color}" font-size="10.5" font-weight="bold">${label}</text>` : ""}`,
  );
}
