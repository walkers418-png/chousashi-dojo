// パターン図解 — 「答えがパターンで分かれる」論点を、順番に動く図解（ステップ）で示す。
// 各要素は data-from（この手順から表示）/ data-until（この手順まで表示）で出し入れし、
// app.js の renderPattern が手順を進めて動画風に見せる。勘違いしやすい型は最後に大きな×。
// 図はSVG。法律関係（当事者A/B/C・譲渡・取消・登記）を箱と矢印で表す。

// ---- SVGビルダー（座標系 viewBox 0 0 360 300）----
function _g(from, until, inner) {
  const u = until === undefined ? "" : ` data-until="${until}"`;
  return `<g data-from="${from}"${u}>${inner}</g>`;
}
function _box(x, y, title, sub, from, until, hi) {
  return _g(
    from,
    until,
    `<rect x="${x}" y="${y}" width="92" height="46" rx="9" fill="${hi ? "#10243a" : "#232b33"}" stroke="${hi ? "#4fc3f7" : "#2e3942"}" stroke-width="2"/>
     <text x="${x + 46}" y="${y + 21}" text-anchor="middle" fill="#e8edf2" font-size="14" font-weight="bold">${title}</text>
     <text x="${x + 46}" y="${y + 37}" text-anchor="middle" fill="#8fa0ae" font-size="10">${sub || ""}</text>`,
  );
}
function _arrow(x1, y1, x2, y2, label, from, until, color) {
  color = color || "#8fa0ae";
  const mx = (x1 + x2) / 2,
    my = (y1 + y2) / 2;
  return _g(
    from,
    until,
    `<line x1="${x1}" y1="${y1}" x2="${x2}" y2="${y2}" stroke="${color}" stroke-width="2.5" marker-end="url(#ah)"/>
     <text x="${mx}" y="${my - 7}" text-anchor="middle" fill="${color}" font-size="11.5" font-weight="bold">${label}</text>`,
  );
}
function _note(x, y, text, from, until, color) {
  color = color || "#e8edf2";
  return _g(
    from,
    until,
    `<text x="${x}" y="${y}" fill="${color}" font-size="12.5">${text}</text>`,
  );
}
function _mark(x, y, type, from, until) {
  const c = type === "x" ? "#ef5350" : "#66bb6a";
  const sym = type === "x" ? "✕" : "◯";
  return _g(
    from,
    until,
    `<text x="${x}" y="${y}" text-anchor="middle" fill="${c}" font-size="72" font-weight="bold" opacity="0.92">${sym}</text>`,
  );
}
function _svg(inner) {
  return `<svg viewBox="0 0 360 300" xmlns="http://www.w3.org/2000/svg" class="patsvg" preserveAspectRatio="xMidYMid meet">
    <defs><marker id="ah" markerWidth="9" markerHeight="9" refX="7" refY="3" orient="auto"><path d="M0,0 L7,3 L0,6 Z" fill="#8fa0ae"/></marker></defs>
    ${inner}</svg>`;
}

